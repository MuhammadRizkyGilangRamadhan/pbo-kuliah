    public MataKuliahAddFrame(int id) {
        initComponents();
        try {
            Connection koneksi = Basisdata.getConnection();
            String findSQL = "SELECT * FROM matakuliah WHERE id_mata_kuliah="+id;
            Statement statement = koneksi.createStatement();
            ResultSet resultSet = statement.executeQuery(findSQL);
            while(resultSet.next()){
                idmatakuliahTextField.setText(resultSet.getString("id_mata_kuliah"));
                namamatakuliahTextField.setText(resultSet.getString("nama_mata_kuliah"));
                sksTextField.setText(resultSet.getString("sks"));
                semesterTextField.setText(resultSet.getString("semester"));
            }
            koneksi.close();
        } catch (SQLException ex) {
            System.err.println(ex.getMessage());
        }
    }
    

