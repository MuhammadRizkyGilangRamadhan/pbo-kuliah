    private void simpanButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanButtonActionPerformed
        dispose();
        if(idmatakuliahTextField.getText().equals("")){
            try {
                Connection koneksi = Basisdata.getConnection();
                String insertSQL = "INSERT INTO matakuliah SET " + "nama_mata_kuliah = '" + namamatakuliahTextField.getText() +"'"
                + "sks= '" +
                sksTextField.getText() +"'" +
                "semester = '" +
                semesterTextField.getText() +"'" ;
                Statement statement = koneksi.createStatement();
                statement.executeUpdate(insertSQL);
                koneksi.close();
                dispose();
            } catch (SQLException ex) {
                System.err.println(ex.getMessage());
            }
        } else {
            try {
                Connection koneksi = Basisdata.getConnection();
                String updateSQL = "UPDATE matakuliah SET nama = '" +
                namamatakuliahTextField.getText() +
                "alamat = '" +
                sksTextField.getText() +"'";
                Statement statement = koneksi.createStatement();
                statement.executeUpdate(updateSQL);
                koneksi.close();
                dispose();
            } catch (SQLException ex) {
                System.err.println(ex.getMessage());
            }
        }
    }//GEN-LAST:event_simpanButtonActionPerformed

